void delay_cpu(unsigned long delay_value) {
    unsigned  int dly;
	for (dly=0; dly!=delay_value; dly++){
		asm("nop");
	}
}

void delay_ms(unsigned int delay){
	while(delay){
		delay_cpu(NOPS_TO_MS);
		delay--;
	}	
}


